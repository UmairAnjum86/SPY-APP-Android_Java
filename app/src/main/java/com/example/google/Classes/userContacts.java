package com.example.google.Classes;

public class userContacts {
    String name;
    String phoneno;

    public userContacts() {
    }

    public userContacts(String name, String phoneno) {
        this.name = name;
        this.phoneno = phoneno;
    }

    public String getName() {
        return name;
    }

    public String getPhoneno() {
        return phoneno;
    }
}
